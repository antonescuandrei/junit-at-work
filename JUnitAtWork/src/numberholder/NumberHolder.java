package numberholder;

/**
 * The NumberHolder class of the assignment.
 */
public class NumberHolder {
    public int anInt;
    public float aFloat;
    
    /**
     * Getter for the number holder's int value.
     * @return the int value stored in the number holder
     */
    int getAnInt() {
        return anInt;
    }
    
    /**
     * Getter for the number holder's float value.
     * @return the float value stored in the number holder
     */
    float getAFloat() {
        return aFloat;
    }
}
